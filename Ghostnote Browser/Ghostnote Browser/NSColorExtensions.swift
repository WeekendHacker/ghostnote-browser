import Foundation
import Cocoa

extension NSColor {
    
    static func gnBlue() -> NSColor {
        return NSColor(netHex: 0x3C75B8)
    }
    
    
//    static func gnDarkBlue() -> NSColor {
//        return NSColor(netHex:0x3C75B8)
//    }
    
    static func gnGrey() -> NSColor {
        return NSColor(netHex: 0xE5E3E3)
    }
    
    static func gnWhite() -> NSColor {
        return NSColor(netHex: 0xc5c5c5)
    }
}